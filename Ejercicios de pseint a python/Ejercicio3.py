print("Diseñar un algoritmo que imprima el cuadrado y el cubo de un número ingresado por teclado. ")
numero = int(input("Ingrese un número para calcular su cuadrado y su cubo: "))
cuadrado = numero * numero
cubo = numero ** 3
print("El cuadrado de", numero, "es :", cuadrado)
print("El cubo de", numero, "es :", cubo)